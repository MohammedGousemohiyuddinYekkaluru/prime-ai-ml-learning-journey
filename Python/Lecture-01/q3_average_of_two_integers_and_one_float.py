# Ask the user to enter two integers ans one float. Convert them all to floats and print their average.

num_1 = int(input("Enter one integer number : "))
num_2 = int(input("Enter another integer number : "))
num_3 = float(input("Enter one decimal/float number : "))

average = (num_1 + num_2 + num_3) / 3

print(average)